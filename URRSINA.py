def crear_niveles(nivel1):
    largoHabitacion=len(nivel1)
    anchoHabitacion=len(nivel1[0])
    xInicio=anchoHabitacion/2
    # Crear un espacio, como una sala
    for z in range(largoHabitacion):
        piso = nivel1[z]
        techo = nivel1[z]
        # crear el suelo
        Caja3D(position=(xInicio,0,z),escala= (anchoHabitacion,1))
        # crear los bloques con su altura
        for x in range (anchoHabitacion):
            # crecimiento del suelo
            if piso[x] >0:
                Caja3D(position=(x,piso[x],z),escala= (1, piso[x]))
            # valida si hay techo
            if techo[x] > 0:
                Caja3D(position=(x,techo[x],z),escala= (1,1))
