class Caja3D(Button):
    def __init__(self,position = (0,0,0), escala=(0,0)):
        # Determinar la textura a utilizar
        if position[1] == 0:  # Suelo
            textura = textura_suelo
        else:  # Paredes
            textura = textura_pared 
        
        super().__init__(
            parent = scene,
            position=position,
            model='cube',
            origin_y=0.5,
            collider='box',
            texture=textura,
            color=color.color(0,0,random.uniform(0.9,1)),
            scale=escala,
            highlight_color=color.lime )



